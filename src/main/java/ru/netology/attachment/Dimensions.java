package ru.netology.attachment;

public class Dimensions {
    private int width;//ширина в миллиметрах
    private int height;//высота в миллиметрах

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    private int length;//длина в миллиметрах

}
